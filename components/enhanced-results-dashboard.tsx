"use client"

import { motion } from "framer-motion"
import { useState } from "react"
import { ArrowLeft, Download, Share2, FileText, Brain, AlertTriangle, TrendingUp, BookOpen } from "lucide-react"
import type { DocumentAnalysis } from "@/lib/types"
import type { EnhancedAnalysis } from "@/lib/ai-analysis-engine"
import { SummaryCard } from "./summary-card"
import { RiskAnalysis } from "./risk-analysis"
import { DocumentInsights } from "./document-insights"
import { RecommendationsPanel } from "./recommendations-panel"
import { Button } from "@/components/ui/button"
import { Card } from "@/components/ui/card"
import { Badge } from "@/components/ui/badge"

interface EnhancedResultsDashboardProps {
  analysis: DocumentAnalysis | EnhancedAnalysis
  onReset: () => void
  showBackButton?: boolean
}

export function EnhancedResultsDashboard({ analysis, onReset, showBackButton = false }: EnhancedResultsDashboardProps) {
  const [activeTab, setActiveTab] = useState<"summary" | "risks" | "insights" | "recommendations">("summary")

  // Check if this is an enhanced analysis
  const isEnhanced = "keyTerms" in analysis
  const enhancedAnalysis = isEnhanced ? (analysis as EnhancedAnalysis) : null

  const handleExport = () => {
    const exportData = {
      summary: analysis.summary,
      riskCount: analysis.riskyClausesList.length,
      risks: analysis.riskyClausesList.map((clause) => ({
        text: clause.text,
        riskLevel: clause.riskLevel,
        category: clause.category,
        explanation: clause.explanation,
      })),
      ...(enhancedAnalysis && {
        documentType: enhancedAnalysis.documentType,
        keyTerms: enhancedAnalysis.keyTerms,
        readabilityScore: enhancedAnalysis.readabilityScore,
        legalComplexity: enhancedAnalysis.legalComplexity,
        sentimentAnalysis: enhancedAnalysis.sentimentAnalysis,
        recommendations: enhancedAnalysis.recommendations,
      }),
      analyzedAt: analysis.uploadedAt.toISOString(),
    }

    const blob = new Blob([JSON.stringify(exportData, null, 2)], {
      type: "application/json",
    })
    const url = URL.createObjectURL(blob)
    const a = document.createElement("a")
    a.href = url
    a.download = `legal-analysis-${Date.now()}.json`
    document.body.appendChild(a)
    a.click()
    document.body.removeChild(a)
    URL.revokeObjectURL(url)
  }

  const handleShare = async () => {
    const shareText = `Legal Document Analysis Results:

Summary: ${analysis.summary}

Risks Found: ${analysis.riskyClausesList.length}
${enhancedAnalysis ? `Document Type: ${enhancedAnalysis.documentType}` : ""}
${enhancedAnalysis ? `Legal Complexity: ${enhancedAnalysis.legalComplexity}` : ""}

Generated by Legal AI Analyzer`

    if (navigator.share) {
      try {
        await navigator.share({
          title: "Legal Document Analysis",
          text: shareText,
        })
      } catch (error) {
        console.log("Share cancelled or failed")
      }
    } else {
      await navigator.clipboard.writeText(shareText)
      alert("Analysis copied to clipboard!")
    }
  }

  const getComplexityColor = (complexity: string) => {
    switch (complexity) {
      case "high":
        return "bg-red-100 text-red-800 dark:bg-red-900/20 dark:text-red-400"
      case "medium":
        return "bg-yellow-100 text-yellow-800 dark:bg-yellow-900/20 dark:text-yellow-400"
      case "low":
        return "bg-green-100 text-green-800 dark:bg-green-900/20 dark:text-green-400"
      default:
        return "bg-gray-100 text-gray-800 dark:bg-gray-900/20 dark:text-gray-400"
    }
  }

  const getSentimentColor = (sentiment: string) => {
    switch (sentiment) {
      case "positive":
        return "bg-green-100 text-green-800 dark:bg-green-900/20 dark:text-green-400"
      case "negative":
        return "bg-red-100 text-red-800 dark:bg-red-900/20 dark:text-red-400"
      default:
        return "bg-gray-100 text-gray-800 dark:bg-gray-900/20 dark:text-gray-400"
    }
  }

  return (
    <motion.div
      initial={{ opacity: 0 }}
      animate={{ opacity: 1 }}
      transition={{ duration: 0.5 }}
      className="min-h-screen bg-background"
    >
      {/* Header */}
      <motion.div
        initial={{ opacity: 0, y: -20 }}
        animate={{ opacity: 1, y: 0 }}
        className="bg-card border-b border-border"
      >
        <div className="max-w-7xl mx-auto px-6 py-6">
          <div className="flex items-center justify-between">
            <div className="flex items-center space-x-4">
              <Button variant="outline" onClick={onReset} className="gap-2 bg-transparent">
                <ArrowLeft className="h-4 w-4" />
                {showBackButton ? "Back to Dashboard" : "New Analysis"}
              </Button>
              <div className="h-6 w-px bg-border" />
              <div>
                <h1 className="text-3xl font-bold text-foreground">Analysis Complete</h1>
                <div className="flex items-center space-x-4 mt-1">
                  <p className="text-muted-foreground">
                    {analysis.originalText.length.toLocaleString()} characters analyzed
                  </p>
                  {enhancedAnalysis && (
                    <>
                      <Badge className={getComplexityColor(enhancedAnalysis.legalComplexity)}>
                        {enhancedAnalysis.legalComplexity} complexity
                      </Badge>
                      <Badge variant="outline">{enhancedAnalysis.documentType}</Badge>
                    </>
                  )}
                </div>
              </div>
            </div>

            <div className="flex items-center space-x-3">
              <Button variant="outline" onClick={handleShare} className="gap-2 bg-transparent">
                <Share2 className="h-4 w-4" />
                Share
              </Button>
              <Button onClick={handleExport} className="gap-2">
                <Download className="h-4 w-4" />
                Export
              </Button>
            </div>
          </div>
        </div>
      </motion.div>

      <div className="max-w-7xl mx-auto px-6 py-8">
        {/* Enhanced Stats Overview */}
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ delay: 0.1 }}
          className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-6 mb-8"
        >
          <Card className="p-6">
            <div className="flex items-center justify-between">
              <div>
                <p className="text-sm font-medium text-muted-foreground">Risk Level</p>
                <p className="text-2xl font-bold text-foreground">
                  {analysis.riskyClausesList.filter((c) => c.riskLevel === "high").length > 0
                    ? "High"
                    : analysis.riskyClausesList.length > 0
                      ? "Medium"
                      : "Low"}
                </p>
              </div>
              <div className="p-3 bg-red-100 dark:bg-red-900/20 rounded-lg">
                <AlertTriangle className="h-6 w-6 text-red-600 dark:text-red-400" />
              </div>
            </div>
          </Card>

          <Card className="p-6">
            <div className="flex items-center justify-between">
              <div>
                <p className="text-sm font-medium text-muted-foreground">Total Risks</p>
                <p className="text-2xl font-bold text-foreground">{analysis.riskyClausesList.length}</p>
              </div>
              <div className="p-3 bg-yellow-100 dark:bg-yellow-900/20 rounded-lg">
                <FileText className="h-6 w-6 text-yellow-600 dark:text-yellow-400" />
              </div>
            </div>
          </Card>

          {enhancedAnalysis && (
            <>
              <Card className="p-6">
                <div className="flex items-center justify-between">
                  <div>
                    <p className="text-sm font-medium text-muted-foreground">Readability</p>
                    <p className="text-2xl font-bold text-foreground">{enhancedAnalysis.readabilityScore}/100</p>
                  </div>
                  <div className="p-3 bg-blue-100 dark:bg-blue-900/20 rounded-lg">
                    <BookOpen className="h-6 w-6 text-blue-600 dark:text-blue-400" />
                  </div>
                </div>
              </Card>

              <Card className="p-6">
                <div className="flex items-center justify-between">
                  <div>
                    <p className="text-sm font-medium text-muted-foreground">Sentiment</p>
                    <Badge className={getSentimentColor(enhancedAnalysis.sentimentAnalysis.overall)}>
                      {enhancedAnalysis.sentimentAnalysis.overall}
                    </Badge>
                  </div>
                  <div className="p-3 bg-green-100 dark:bg-green-900/20 rounded-lg">
                    <TrendingUp className="h-6 w-6 text-green-600 dark:text-green-400" />
                  </div>
                </div>
              </Card>
            </>
          )}
        </motion.div>

        {/* Tab Navigation */}
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ delay: 0.2 }}
          className="flex space-x-1 bg-muted p-1 rounded-lg mb-6 w-fit"
        >
          <button
            onClick={() => setActiveTab("summary")}
            className={`px-6 py-2 rounded-md font-medium transition-all flex items-center gap-2 ${
              activeTab === "summary"
                ? "bg-background text-foreground shadow-sm"
                : "text-muted-foreground hover:text-foreground"
            }`}
          >
            <FileText className="h-4 w-4" />
            Summary
          </button>
          <button
            onClick={() => setActiveTab("risks")}
            className={`px-6 py-2 rounded-md font-medium transition-all relative flex items-center gap-2 ${
              activeTab === "risks"
                ? "bg-background text-foreground shadow-sm"
                : "text-muted-foreground hover:text-foreground"
            }`}
          >
            <AlertTriangle className="h-4 w-4" />
            Risk Analysis
            {analysis.riskyClausesList.length > 0 && (
              <Badge variant="destructive" className="h-5 w-5 p-0 text-xs flex items-center justify-center">
                {analysis.riskyClausesList.length}
              </Badge>
            )}
          </button>
          {enhancedAnalysis && (
            <>
              <button
                onClick={() => setActiveTab("insights")}
                className={`px-6 py-2 rounded-md font-medium transition-all flex items-center gap-2 ${
                  activeTab === "insights"
                    ? "bg-background text-foreground shadow-sm"
                    : "text-muted-foreground hover:text-foreground"
                }`}
              >
                <Brain className="h-4 w-4" />
                Insights
              </button>
              <button
                onClick={() => setActiveTab("recommendations")}
                className={`px-6 py-2 rounded-md font-medium transition-all flex items-center gap-2 ${
                  activeTab === "recommendations"
                    ? "bg-background text-foreground shadow-sm"
                    : "text-muted-foreground hover:text-foreground"
                }`}
              >
                <TrendingUp className="h-4 w-4" />
                Recommendations
                <Badge variant="secondary" className="h-5 w-5 p-0 text-xs flex items-center justify-center">
                  {enhancedAnalysis.recommendations.length}
                </Badge>
              </button>
            </>
          )}
        </motion.div>

        {/* Content */}
        <motion.div
          key={activeTab}
          initial={{ opacity: 0, x: 20 }}
          animate={{ opacity: 1, x: 0 }}
          transition={{ duration: 0.3 }}
        >
          {activeTab === "summary" && (
            <SummaryCard summary={analysis.summary} originalLength={analysis.originalText.length} />
          )}
          {activeTab === "risks" && (
            <RiskAnalysis clauses={analysis.riskyClausesList} originalText={analysis.originalText} />
          )}
          {activeTab === "insights" && enhancedAnalysis && <DocumentInsights analysis={enhancedAnalysis} />}
          {activeTab === "recommendations" && enhancedAnalysis && (
            <RecommendationsPanel recommendations={enhancedAnalysis.recommendations} analysis={enhancedAnalysis} />
          )}
        </motion.div>
      </div>
    </motion.div>
  )
}
